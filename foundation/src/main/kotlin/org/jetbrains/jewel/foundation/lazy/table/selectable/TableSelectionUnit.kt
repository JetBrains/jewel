package org.jetbrains.jewel.foundation.lazy.table.selectable

public enum class TableSelectionUnit {
    /**
     * Selects a single cell.
     */
    Cell,

    /**
     * Selects a row.
     */
    Row,

    /**
     * Selects a column.
     */
    Column,

    /**
     * Selects all cells.
     */
    All,
}
